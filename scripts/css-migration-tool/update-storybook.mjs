import path from 'node:path';
import { fileURLToPath } from 'node:url';

import { absFrom, listByGlob, readPackageJson, readText, toPosixPath, writeText } from './fs.mjs';

const thisDir = path.dirname(fileURLToPath(import.meta.url));
const missingTokensStoriesTemplatePath = path.join(thisDir, 'missing-tokens.stories.template.tsx');
import {
    extractFirstBlockCommentBody,
    extractMissingTokensFromTopSummaryNoteBody,
    uniqSorted,
} from './notes.mjs';

const LEGACY = /withMissingTokenStoryTitle\(\s*'((?:[^'\\]|\\.)*)'\s*,\s*'([^']*)'\s*\)/g;

function normalizeTitlePath(val) {
    let v = val.replace(/^⚠️\s+/, '').trim();
    const parts = v.split('/');
    if (parts.length > 0) {
        parts[parts.length - 1] = parts[parts.length - 1].replace(/^⚠️\s+/, '').trim();
    }
    return parts.join('/');
}

function buildTitleWithEmojiOnComponentName(basePath, needs) {
    const parts = basePath.split('/');
    if (parts.length === 0) {
        return basePath;
    }
    let last = parts[parts.length - 1];
    if (needs) {
        last = last.replace(/^⚠️\s+/, '').trim();
        last = last.replace(/^🔧\s+/, '').trim();
        parts[parts.length - 1] = `⚠️ ${last}`;
        return parts.join('/');
    }
    return basePath;
}

function storyDirHasMissingTokens(storyDir, configPaths) {
    if (configPaths.some((p) => p.startsWith(`${storyDir}/`))) {
        return true;
    }
    if (storyDir.startsWith('Tokens/')) {
        const last = storyDir.split('/').pop() ?? '';
        return configPaths.some((p) => p.startsWith(`${last}/`));
    }
    return false;
}

function applyTitleEmoji(content, storyDir, configPaths) {
    let replacedLegacy = content.replace(LEGACY, (_, title, dir) => {
        const base = normalizeTitlePath(title);
        const needs = storyDirHasMissingTokens(dir, configPaths);
        const next = buildTitleWithEmojiOnComponentName(base, needs);
        return JSON.stringify(next);
    });
    replacedLegacy = replacedLegacy.replace(/\nimport \{ withMissingTokenStoryTitle \} from ['"][^'"]+['"];?\n?/g, '\n');
    replacedLegacy = replacedLegacy.replace(/^import \{ withMissingTokenStoryTitle \} from ['"][^'"]+['"];?\n?/gm, '');

    let done = false;
    const out = replacedLegacy.replace(/title:\s*(["'])([^"']*)\1/g, (full, q, val) => {
        if (done) {
            return full;
        }
        if (!val.includes('/')) {
            return full;
        }
        done = true;
        const base = normalizeTitlePath(val);
        const needs = storyDirHasMissingTokens(storyDir, configPaths);
        const next = buildTitleWithEmojiOnComponentName(base, needs);
        return `title: ${q}${next}${q}`;
    });

    return out;
}

export function buildMissingTokensDescription({ override, packageName, tokensImport }) {
    if (override && String(override).trim()) {
        return String(override).trim();
    }
    const name = (packageName && String(packageName).trim()) || 'this package';
    const parts = [
        `Package ${name}: token identifiers gathered from Storybook component config NOTE blocks that list missing design tokens.`,
    ];
    if (tokensImport && String(tokensImport).trim()) {
        parts.push(`Primary token module for this package: ${String(tokensImport).trim()}.`);
    }
    parts.push(
        'Configs may use alternate imports or raw CSS variables where a name is not exported from that module.',
    );
    return parts.join(' ');
}

function buildMissingTokensDataTs({ generatedAt, entries, byToken, packageNote }) {
    return `/* eslint-disable */
// Generated by scripts/css-migration-tool.mjs — do not edit by hand.
// Regenerates with MissingTokens.stories.tsx from the same command:
// node scripts/css-migration-tool.mjs update-storybook --package-root <path> [--tokens-import <spec>]

export type MissingTokenEntry = {
    configPath: string;
    tokens: string[];
};

export const missingTokensGeneratedAt = ${JSON.stringify(generatedAt)};

export const missingTokensDescription = ${JSON.stringify(packageNote)};

export const missingTokensByConfig: MissingTokenEntry[] = ${JSON.stringify(entries, null, 4)};

/** Token name → config files that mention it as missing */
export const missingTokensByToken: Record<string, string[]> = ${JSON.stringify(byToken, null, 4)};
`;
}

export async function updateStorybook({
    packageRoot,
    only = 'all', // all | data | titles
    packageName: packageNameOpt,
    tokensImport,
    missingTokensDescription: descriptionOverride,
}) {
    const pkgRootAbs = absFrom(process.cwd(), packageRoot);
    const componentsDir = path.join(pkgRootAbs, 'src', 'components');
    const outFile = path.join(componentsDir, 'Tokens', 'MissingTokens', 'missingTokensData.ts');
    const storiesOutFile = path.join(componentsDir, 'Tokens', 'MissingTokens', 'MissingTokens.stories.tsx');

    const configFiles = await listByGlob({
        cwd: pkgRootAbs,
        patterns: ['src/components/**/*.config.ts', 'src/components/**/*.config.tsx'],
    });

    const entries = [];
    for (const absConfig of configFiles) {
        const source = await readText(absConfig);
        const body = extractFirstBlockCommentBody(source);
        if (!body || !body.includes('Missing tokens in @salutejs/sdds-themes/tokens')) continue;
        const tokens = extractMissingTokensFromTopSummaryNoteBody(body);
        if (tokens.length === 0) continue;
        const rel = toPosixPath(path.relative(componentsDir, absConfig));
        entries.push({ configPath: rel, tokens });
    }
    entries.sort((a, b) => a.configPath.localeCompare(b.configPath));

    const tokenToConfigs = new Map();
    for (const { configPath, tokens } of entries) {
        for (const t of tokens) {
            if (!tokenToConfigs.has(t)) tokenToConfigs.set(t, []);
            tokenToConfigs.get(t).push(configPath);
        }
    }
    for (const [, paths] of tokenToConfigs) {
        paths.sort((a, b) => a.localeCompare(b));
    }

    const uniqueTokens = uniqSorted([...tokenToConfigs.keys()]);
    const byToken = Object.fromEntries(uniqueTokens.map((t) => [t, tokenToConfigs.get(t)]));

    let packageName = packageNameOpt;
    if (!packageName) {
        try {
            const pkg = await readPackageJson(pkgRootAbs);
            if (pkg && typeof pkg.name === 'string') packageName = pkg.name;
        } catch {
            packageName = undefined;
        }
    }

    if (only === 'all' || only === 'data') {
        const packageNote = buildMissingTokensDescription({
            override: descriptionOverride,
            packageName,
            tokensImport,
        });
        const ts = buildMissingTokensDataTs({
            generatedAt: new Date().toISOString(),
            entries,
            byToken,
            packageNote,
        });
        await writeText(outFile, ts);

        const storiesHeader = `// Generated by scripts/css-migration-tool.mjs — do not edit by hand.
// Regenerate with: node scripts/css-migration-tool.mjs update-storybook --package-root <path>
// (writes this file and missingTokensData.ts in Tokens/MissingTokens/)

`;
        const storiesTemplate = await readText(missingTokensStoriesTemplatePath);
        await writeText(storiesOutFile, storiesHeader + storiesTemplate);
    }

    let updatedStories = 0;
    if (only === 'all' || only === 'titles') {
        const configPaths = entries.map((e) => e.configPath);
        const storyFiles = await listByGlob({
            cwd: pkgRootAbs,
            patterns: ['src/components/**/*.stories.tsx'],
        });

        for (const absStory of storyFiles) {
            const before = await readText(absStory);
            const rel = toPosixPath(path.relative(componentsDir, absStory));
            const storyDir = path.posix.dirname(rel);
            const after = applyTitleEmoji(before, storyDir, configPaths);
            if (after !== before) {
                await writeText(absStory, after);
                updatedStories++;
            }
        }
    }

    return {
        stats: {
            configsScanned: configFiles.length,
            configsWithMissing: entries.length,
            uniqueTokens: uniqueTokens.length,
            updatedStories,
            outFile,
            storiesOutFile: only === 'all' || only === 'data' ? storiesOutFile : null,
        },
    };
}

